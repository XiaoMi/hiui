async function pathExists(targetPath) {
  try {
    await fs.access(targetPath)
    return true
  } catch {
    return false
  }
}

async function readJsonIfExists(filePath) {
  try {
    return JSON.parse(await fs.readFile(filePath, 'utf8'))
  } catch {
    return null
  }
}

async function readTextIfExists(filePath) {
  try {
    return await fs.readFile(filePath, 'utf8')
  } catch {
    return ''
  }
}

function sanitizeProjectSlug(input) {
  const raw = String(input || '')
    .trim()
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-+|-+$/g, '')

  if (!raw) {
    return 'project'
  }

  return /^[0-9]/.test(raw) ? `project-${raw}` : raw
}

async function resolveProjectSlug(targetRoot) {
  const packageJson = await readJsonIfExists(path.join(targetRoot, 'package.json'))
  if (packageJson?.name) {
    return sanitizeProjectSlug(packageJson.name)
  }
  return sanitizeProjectSlug(path.basename(targetRoot))
}

